const String _kFaceVerifyUrl =
    'https://pasteshub404-navikarana-backend.hf.space/login-face';

class AdminScanPage extends StatefulWidget {
  final String eventId;
  final String eventTitle;
  final String adminId;

  const AdminScanPage({
    super.key,
    required this.eventId,
    required this.eventTitle,
    required this.adminId,
  });

  @override
  State<AdminScanPage> createState() => _AdminScanPageState();
}

class _AdminScanPageState extends State<AdminScanPage> {
  final MobileScannerController _scanCtrl = MobileScannerController(
    detectionSpeed: DetectionSpeed.noDuplicates,
  );
  final _manualCtrl = TextEditingController();

  bool _processing = false;
  _ScanFeedback? _feedback;
  bool _sheetOpen = false; // pause scanner while sheet is visible

  @override
  void dispose() {
    _scanCtrl.dispose();
    _manualCtrl.dispose();
    super.dispose();
  }

  // ── QR decoded ─────────────────────────────────────────────────────────────

  Future<void> _handleRawQr(String raw) async {
    if (_processing || _sheetOpen) return;
    final userId = DistributionService.decodeQr(raw);
    if (userId == null) {
      _showFeedback(_ScanFeedback(
        status: _FeedbackStatus.error,
        headline: "Invalid QR Code",
        sub: "Not generated by upasthiti.",
      ));
      return;
    }
    await _openVerificationSheet(userId);
  }

  // ── Load user & open sheet ──────────────────────────────────────────────────

  Future<void> _openVerificationSheet(String userId) async {
    if (_sheetOpen) return;
    setState(() {
      _sheetOpen = true;
      _processing = true;
    });

    // Fetch user details from Appwrite
    Map<String, dynamic>? userData;
    try {
      final res = await AppwriteService.databases.listDocuments(
        databaseId: '69ecebfb0033cf785741',
        collectionId: 'users',
        queries: [Query.equal('username', userId), Query.limit(1)],
      );
      if (res.documents.isNotEmpty) userData = res.documents.first.data;
    } catch (_) {}

    if (!mounted) return;
    setState(() => _processing = false);

    await showModalBottomSheet(
      context: context,
      isScrollControlled: true,
      backgroundColor: Colors.transparent,
      builder: (ctx) => _UserVerificationSheet(
        userId: userId,
        userData: userData,
        eventId: widget.eventId,
        adminId: widget.adminId,
        onIssued: (feedback) {
          Navigator.pop(ctx);
          _showFeedback(feedback);
        },
      ),
    );

    if (mounted) setState(() => _sheetOpen = false);
  }

  void _submitManual() {
    final id = _manualCtrl.text.trim();
    if (id.isNotEmpty) _openVerificationSheet(id);
  }

  void _showFeedback(_ScanFeedback fb) {
    if (!mounted) return;
    setState(() => _feedback = fb);
    Future.delayed(const Duration(seconds: 5), () {
      if (mounted) setState(() => _feedback = null);
    });
  }

  // ── Build ───────────────────────────────────────────────────────────────────

  @override
  Widget build(BuildContext context) {
    final isWindows = Platform.isWindows;

    return Scaffold(
      backgroundColor: const Color(0xFF101010),
      appBar: AppBar(
        backgroundColor: Colors.transparent,
        elevation: 0,
        foregroundColor: Colors.white,
        title: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text("Scan QR Codes",
                style: GoogleFonts.poppins(
                    fontWeight: FontWeight.bold, fontSize: 16)),
            Text(widget.eventTitle,
                style: GoogleFonts.poppins(fontSize: 11, color: Colors.white60),
                overflow: TextOverflow.ellipsis),
          ],
        ),
        actions: [
          if (!isWindows)
            IconButton(
              icon: const Icon(Icons.flash_on),
              tooltip: "Toggle torch",
              onPressed: () => _scanCtrl.toggleTorch(),
            ),
        ],
      ),
      body: Stack(
        children: [
          isWindows ? _buildWindowsFallback() : _buildCamera(),
          Positioned(
            left: 16,
            right: 16,
            bottom: 32,
            child: AnimatedSwitcher(
              duration: const Duration(milliseconds: 300),
              child: _feedback != null
                  ? _FeedbackBanner(
                      key: ValueKey(_feedback.hashCode), feedback: _feedback!)
                  : _processing
                      ? const _ProcessingBanner(key: ValueKey('proc'))
                      : const SizedBox.shrink(key: ValueKey('empty')),
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildCamera() {
    return Stack(
      children: [
        MobileScanner(
          controller: _scanCtrl,
          onDetect: (capture) {
            for (final barcode in capture.barcodes) {
              final raw = barcode.rawValue;
              if (raw != null) _handleRawQr(raw);
            }
          },
        ),
        Center(
          child: Container(
            width: 240,
            height: 240,
            decoration: BoxDecoration(
              border: Border.all(color: Colors.white, width: 2),
              borderRadius: BorderRadius.circular(16),
            ),
            child: Stack(children: _corners()),
          ),
        ),
        Align(
          alignment: const Alignment(0, -0.25),
          child: Text("Point at recipient's QR code",
              style: GoogleFonts.poppins(color: Colors.white70, fontSize: 13)),
        ),
        Align(
          alignment: const Alignment(0, 0.65),
          child: TextButton.icon(
            onPressed: _showManualEntryDialog,
            icon: const Icon(Icons.keyboard_alt_outlined, color: Colors.white54),
            label: Text("Enter ID manually",
                style:
                    GoogleFonts.poppins(color: Colors.white54, fontSize: 12)),
          ),
        ),
      ],
    );
  }

  Widget _buildWindowsFallback() {
    return Center(
      child: Padding(
        padding: const EdgeInsets.all(32),
        child: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            const Icon(Icons.qr_code_scanner, size: 80, color: Colors.white30),
            const SizedBox(height: 20),
            Text(
              "Camera scanning unavailable on desktop.\nEnter the recipient's unique ID below.",
              textAlign: TextAlign.center,
              style:
                  GoogleFonts.poppins(color: Colors.white54, fontSize: 14),
            ),
            const SizedBox(height: 24),
            TextField(
              controller: _manualCtrl,
              style: const TextStyle(color: Colors.white),
              textInputAction: TextInputAction.done,
              onSubmitted: (_) => _submitManual(),
              decoration: InputDecoration(
                hintText: "Recipient Unique ID",
                hintStyle: const TextStyle(color: Colors.white38),
                filled: true,
                fillColor: const Color(0xFF1E1E1E),
                border: OutlineInputBorder(
                    borderRadius: BorderRadius.circular(14),
                    borderSide: BorderSide.none),
                focusedBorder: OutlineInputBorder(
                    borderRadius: BorderRadius.circular(14),
                    borderSide: const BorderSide(
                        color: Color(0xFF6A8A73), width: 1.5)),
                prefixIcon:
                    const Icon(Icons.badge_outlined, color: Colors.white38),
              ),
            ),
            const SizedBox(height: 16),
            SizedBox(
              width: double.infinity,
              child: ElevatedButton.icon(
                onPressed: _processing ? null : _submitManual,
                icon: const Icon(Icons.search),
                label: Text("Look Up & Verify",
                    style: GoogleFonts.poppins(fontWeight: FontWeight.bold)),
                style: ElevatedButton.styleFrom(
                  backgroundColor: const Color(0xFF6A8A73),
                  foregroundColor: Colors.white,
                  padding: const EdgeInsets.symmetric(vertical: 14),
                  shape: RoundedRectangleBorder(
                      borderRadius: BorderRadius.circular(14)),
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }

  void _showManualEntryDialog() {
    _manualCtrl.clear();
    showDialog(
      context: context,
      builder: (ctx) => AlertDialog(
        shape:
            RoundedRectangleBorder(borderRadius: BorderRadius.circular(20)),
        title: Text("Manual Entry",
            style: GoogleFonts.poppins(fontWeight: FontWeight.bold)),
        content: TextField(
          controller: _manualCtrl,
          autofocus: true,
          textInputAction: TextInputAction.done,
          onSubmitted: (_) {
            Navigator.pop(ctx);
            _submitManual();
          },
          decoration: const InputDecoration(
            hintText: "Recipient Unique ID",
            prefixIcon: Icon(Icons.badge_outlined),
          ),
        ),
        actions: [
          TextButton(
              onPressed: () => Navigator.pop(ctx),
              child: const Text("Cancel")),
          ElevatedButton(
            style: ElevatedButton.styleFrom(
                backgroundColor: const Color(0xFF6A8A73),
                foregroundColor: Colors.white),
            onPressed: () {
              Navigator.pop(ctx);
              _submitManual();
            },
            child: const Text("Look Up"),
          ),
        ],
      ),
    );
  }

  // ── Corner painter helpers ──────────────────────────────────────────────────

  List<Widget> _corners() {
    const c = Color(0xFF6A8A73);
    const size = 20.0;
    const thick = 3.0;
    return [
      Positioned(top: 0, left: 0, child: _corner(c, size, thick, tl: true)),
      Positioned(top: 0, right: 0, child: _corner(c, size, thick, tr: true)),
      Positioned(bottom: 0, left: 0, child: _corner(c, size, thick, bl: true)),
      Positioned(bottom: 0, right: 0, child: _corner(c, size, thick, br: true)),
    ];
  }

  Widget _corner(Color c, double size, double thick,
      {bool tl = false, bool tr = false, bool bl = false, bool br = false}) {
    return SizedBox(
      width: size,
      height: size,
      child: CustomPaint(
        painter: _CornerPainter(
            color: c,
            thickness: thick,
            topLeft: tl,
            topRight: tr,
            bottomLeft: bl,
            bottomRight: br),
      ),
    );
  }
}

