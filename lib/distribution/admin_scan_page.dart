enum _FeedbackStatus { success, warning, error }

class _ScanFeedback {
  final _FeedbackStatus status;
  final String headline;
  final String sub;
  const _ScanFeedback(
      {required this.status, required this.headline, required this.sub});
}

class _FeedbackBanner extends StatelessWidget {
  final _ScanFeedback feedback;
  const _FeedbackBanner({super.key, required this.feedback});

  @override
  Widget build(BuildContext context) {
    final colors = {
      _FeedbackStatus.success: [
        const Color(0xFF1B5E20),
        const Color(0xFF4CAF50)
      ],
      _FeedbackStatus.warning: [
        const Color(0xFF4A3000),
        const Color(0xFFFFB300)
      ],
      _FeedbackStatus.error: [
        const Color(0xFF4A0000),
        const Color(0xFFEF5350)
      ],
    }[feedback.status]!;

    final icons = {
      _FeedbackStatus.success: Icons.check_circle_rounded,
      _FeedbackStatus.warning: Icons.warning_amber_rounded,
      _FeedbackStatus.error: Icons.cancel_rounded,
    }[feedback.status]!;

    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 16),
      decoration: BoxDecoration(
        color: colors[0],
        borderRadius: BorderRadius.circular(16),
        border: Border.all(color: colors[1].withValues(alpha: 0.4)),
      ),
      child: Row(
        children: [
          Icon(icons, color: colors[1], size: 28),
          const SizedBox(width: 14),
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              mainAxisSize: MainAxisSize.min,
              children: [
                Text(feedback.headline,
                    style: GoogleFonts.poppins(
                        color: colors[1],
                        fontWeight: FontWeight.bold,
                        fontSize: 14)),
                Text(feedback.sub,
                    style:
                        GoogleFonts.poppins(color: Colors.white70, fontSize: 12),
                    maxLines: 2,
                    overflow: TextOverflow.ellipsis),
              ],
            ),
          ),
        ],
      ),
    );
  }
}

