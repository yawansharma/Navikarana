class _UserVerificationSheet extends StatefulWidget {
  final String userId;
  final Map<String, dynamic>? userData;
  final String eventId;
  final String adminId;
  final void Function(_ScanFeedback) onIssued;

  const _UserVerificationSheet({
    required this.userId,
    required this.userData,
    required this.eventId,
    required this.adminId,
    required this.onIssued,
  });

  @override
  State<_UserVerificationSheet> createState() => _UserVerificationSheetState();
}

enum _VerifyStep { idle, capturingFace, verifying, issuing }

class _UserVerificationSheetState extends State<_UserVerificationSheet> {
  _VerifyStep _step = _VerifyStep.idle;
  String? _stepMessage;
  File? _capturedFace;

  String get _name =>
      widget.userData?['name'] as String? ?? widget.userId;
  String get _dept =>
      widget.userData?['department'] as String? ?? '—';

  // ── Face capture ────────────────────────────────────────────────────────────

  Future<void> _captureAndVerify() async {
    // Step 1: capture face photo
    if (Platform.isWindows) {
      // Windows: skip face verification, issue directly after confirmation
      await _issuePackage(skipFace: true);
      return;
    }

    setState(() {
      _step = _VerifyStep.capturingFace;
      _stepMessage = "Opening camera...";
    });

    XFile? photo;
    try {
      photo = await ImagePicker().pickImage(
        source: ImageSource.camera,
        preferredCameraDevice: CameraDevice.front,
        maxWidth: 640,
        maxHeight: 640,
        imageQuality: 90,
      );
    } catch (_) {}

    if (photo == null) {
      setState(() {
        _step = _VerifyStep.idle;
        _stepMessage = null;
      });
      return;
    }

    _capturedFace = File(photo.path);

    // Step 2: verify face
    setState(() {
      _step = _VerifyStep.verifying;
      _stepMessage = "Verifying identity...";
    });

    final error = await _verifyFaceOnBackend(_capturedFace!, widget.userId);

    if (!mounted) return;

    if (error != null) {
      setState(() {
        _step = _VerifyStep.idle;
        _stepMessage = null;
      });
      widget.onIssued(_ScanFeedback(
        status: _FeedbackStatus.error,
        headline: "Face Verification Failed",
        sub: error,
      ));
      return;
    }

    // Step 3: issue package
    await _issuePackage(skipFace: false);
  }

  Future<String?> _verifyFaceOnBackend(File photo, String username) async {
    try {
      final request =
          http.MultipartRequest('POST', Uri.parse(_kFaceVerifyUrl));
      request.fields['username'] = username;
      request.files
          .add(await http.MultipartFile.fromPath('image', photo.path));
      final streamed = await request.send();
      final body = await streamed.stream.bytesToString();
      final decoded = jsonDecode(body) as Map<String, dynamic>;
      if (decoded['verified'] == true) return null;
      return decoded['error'] as String? ?? 'Face not recognised.';
    } catch (_) {
      return 'Could not reach the server.';
    }
  }

  Future<void> _issuePackage({required bool skipFace}) async {
    if (!mounted) return;
    setState(() {
      _step = _VerifyStep.issuing;
      _stepMessage = "Issuing package...";
    });

    final result = await DistributionService.processQrScan(
      eventId: widget.eventId,
      scannedUserId: widget.userId,
      adminId: widget.adminId,
    );

    if (!mounted) return;

    _ScanFeedback fb;
    switch (result.status) {
      case ScanStatus.success:
        fb = _ScanFeedback(
          status: _FeedbackStatus.success,
          headline: skipFace
              ? "Package Issued (face skipped on desktop)"
              : "Package Issued ✓ Face Verified",
          sub: result.userName ?? widget.userId,
        );
        break;
      case ScanStatus.alreadyIssued:
        final when = result.issuedAt != null ? _fmt(result.issuedAt!) : 'earlier';
        fb = _ScanFeedback(
          status: _FeedbackStatus.warning,
          headline: "Already Issued",
          sub:
              "${result.userName ?? widget.userId} — $when by ${result.issuedBy ?? '?'}",
        );
        break;
      case ScanStatus.notInList:
        fb = _ScanFeedback(
          status: _FeedbackStatus.error,
          headline: "Not on Recipient List",
          sub: widget.userId,
        );
        break;
      case ScanStatus.revoked:
        fb = _ScanFeedback(
          status: _FeedbackStatus.error,
          headline: "Recipient Revoked",
          sub: result.userName ?? widget.userId,
        );
        break;
      case ScanStatus.eventNotActive:
        fb = _ScanFeedback(
          status: _FeedbackStatus.error,
          headline: "Event Not Active",
          sub: "This event is no longer accepting scans.",
        );
        break;
      case ScanStatus.notAuthorized:
        fb = _ScanFeedback(
          status: _FeedbackStatus.error,
          headline: "Not Authorized",
          sub: "You are not assigned to this event.",
        );
        break;
    }

    widget.onIssued(fb);
  }

  String _fmt(String iso) {
    try {
      final dt = DateTime.parse(iso).toLocal();
      return "${dt.day}/${dt.month} ${dt.hour.toString().padLeft(2, '0')}:${dt.minute.toString().padLeft(2, '0')}";
    } catch (_) {
      return iso;
    }
  }

  // ── Build ───────────────────────────────────────────────────────────────────

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: const BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.vertical(top: Radius.circular(28)),
      ),
      padding: const EdgeInsets.fromLTRB(24, 16, 24, 32),
      child: Column(
        mainAxisSize: MainAxisSize.min,
        children: [
          // Handle
          Center(
            child: Container(
              width: 40,
              height: 4,
              decoration: BoxDecoration(
                  color: Colors.grey.shade300,
                  borderRadius: BorderRadius.circular(2)),
            ),
          ),
          const SizedBox(height: 20),

          // User card
          Container(
            padding: const EdgeInsets.all(16),
            decoration: BoxDecoration(
              color: const Color(0xFFF8F9FB),
              borderRadius: BorderRadius.circular(16),
              border: Border.all(color: Colors.grey.shade200),
            ),
            child: Row(
              children: [
                CircleAvatar(
                  radius: 30,
                  backgroundColor: const Color(0xFF6A8A73).withValues(alpha: 0.15),
                  child: Text(
                    _name.isNotEmpty ? _name[0].toUpperCase() : '?',
                    style: const TextStyle(
                        fontSize: 22,
                        fontWeight: FontWeight.bold,
                        color: Color(0xFF6A8A73)),
                  ),
                ),
                const SizedBox(width: 16),
                Expanded(
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text(_name,
                          style: GoogleFonts.poppins(
                              fontSize: 16, fontWeight: FontWeight.bold)),
                      const SizedBox(height: 2),
                      Text(widget.userId,
                          style: GoogleFonts.poppins(
                              fontSize: 12, color: Colors.grey.shade500)),
                      const SizedBox(height: 2),
                      Text(_dept,
                          style: GoogleFonts.poppins(
                              fontSize: 11, color: Colors.grey.shade500)),
                    ],
                  ),
                ),
                const Icon(Icons.qr_code_2_rounded,
                    color: Color(0xFF6A8A73), size: 28),
              ],
            ),
          ),

          const SizedBox(height: 20),

          // Status / step indicator
          if (_step != _VerifyStep.idle) ...[
            Container(
              padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 12),
              decoration: BoxDecoration(
                color: const Color(0xFF6A8A73).withValues(alpha: 0.08),
                borderRadius: BorderRadius.circular(12),
              ),
              child: Row(
                children: [
                  const SizedBox(
                    width: 18,
                    height: 18,
                    child: CircularProgressIndicator(
                        strokeWidth: 2, color: Color(0xFF6A8A73)),
                  ),
                  const SizedBox(width: 12),
                  Text(_stepMessage ?? '',
                      style: GoogleFonts.poppins(
                          fontSize: 13, color: const Color(0xFF6A8A73))),
                ],
              ),
            ),
            const SizedBox(height: 16),
          ],

          // Face scan instruction
          if (_step == _VerifyStep.idle && !Platform.isWindows) ...[
            Row(
              children: [
                const Icon(Icons.face_retouching_natural,
                    size: 16, color: Colors.grey),
                const SizedBox(width: 6),
                Text(
                  "Face verification required before issuing",
                  style: GoogleFonts.poppins(
                      fontSize: 12, color: Colors.grey.shade600),
                ),
              ],
            ),
            const SizedBox(height: 14),
          ],

          // Action button
          SizedBox(
            width: double.infinity,
            child: ElevatedButton.icon(
              onPressed: _step != _VerifyStep.idle ? null : _captureAndVerify,
              icon: Icon(Platform.isWindows
                  ? Icons.inventory_2_outlined
                  : Icons.face_retouching_natural),
              label: Text(
                Platform.isWindows
                    ? "Issue Package"
                    : "Scan Face & Issue Package",
                style: GoogleFonts.poppins(fontWeight: FontWeight.bold),
              ),
              style: ElevatedButton.styleFrom(
                backgroundColor: const Color(0xFF6A8A73),
                foregroundColor: Colors.white,
                padding: const EdgeInsets.symmetric(vertical: 16),
                shape: RoundedRectangleBorder(
                    borderRadius: BorderRadius.circular(14)),
                elevation: 0,
              ),
            ),
          ),
          const SizedBox(height: 8),
          TextButton(
            onPressed: () => Navigator.pop(context),
            child: Text("Cancel",
                style: GoogleFonts.poppins(color: Colors.grey.shade500)),
          ),
        ],
      ),
    );
  }
}

